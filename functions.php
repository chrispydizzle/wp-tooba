/**
 * Sets up theme defaults and registers support for various WordPress features.
 *
 * Note that this function is hooked into the after_setup_theme hook, which
 * runs before the init hook. The init hook is too late for some features, such
 * as indicating support for post thumbnails.
 */
function tooba_setup() {
	load_theme_textdomain( 'tooba' );
	add_theme_support( 'automatic-feed-links' );
	add_theme_support( 'title-tag' );
	add_theme_support( 'post-thumbnails' );
	add_image_size( 'tooba-featured-image', 2000, 1200, true );
	add_image_size( 'tooba-thumbnail-avatar', 100, 100, true );
	$GLOBALS['content_width'] = 525;
	register_nav_menus( array( 'top' => __( 'Top Menu', 'tooba' ) ) );

	add_theme_support( 'html5', array( 'comment-form', 'comment-list', 'gallery', 'caption', ) );

	add_theme_support( 'post-formats', array( 'aside', 'image', 'video', 'quote', 'link', 'gallery', 'audio', ) );

	// Add theme support for Custom Logo.
	add_theme_support( 'custom-logo', array(
		'height'     => 80,
		'flex-width' => true
	,
	) );

	// Add theme support for selective refresh for widgets.
	add_theme_support( 'customize-selective-refresh-widgets' );

	/*
	 * This theme styles the visual editor to resemble the theme style,
	 * specifically font, colors, and column width.
 	 */
	add_editor_style( array( 'assets/css/editor-style.css', tooba_fonts_url() ) );

	add_shortcodes();
}

add_action( 'after_setup_theme', 'tooba_setup' );
